const Product = require('../models/product');
const Order = require('../models/order');

exports.getProducts = (req, res, next) => {
  //find is one of the built in static methods of mongoose
  //this version of find does not return a cursor
  //if large amount of data add .cursor() to get in chuncks
  Product.find()
    .then(products => {
      console.log(products);
      res.render('shop/product-list', {
        prods: products,
        pageTitle: 'All Products',
        path: '/products'
      });
    })
    .catch(err => {
      const error = new Error(err);
      error.httpStatusCode = 500;
      return next(error);
    });
};

exports.getProduct = (req, res, next) => {
  const prodId = req.params.productId;
  //Mongoose has built in findById method
  //Mongoose automatically converts string ID into ObjectId
  Product.findById(prodId)
    .then(product => {
      res.render('shop/product-detail', {
        product: product,
        pageTitle: product.title,
        path: '/products'
      });
    })
    .catch(err => {
      const error = new Error(err);
      error.httpStatusCode = 500;
      return next(err);
    });
};

